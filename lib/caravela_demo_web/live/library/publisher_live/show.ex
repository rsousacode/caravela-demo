defmodule CaravelaDemoWeb.Library.PublisherLive.Show do
  @moduledoc """
  Generated by Caravela from CaravelaDemo.Domains.Library.

  LiveView that mounts the `library/PublisherShow` Svelte component via
  LiveSvelte. Detail page for a single `publisher`.

  Regenerate with `mix caravela.gen.live CaravelaDemo.Domains.Library`.
  Custom code placed below the `# --- CUSTOM ---` marker is preserved.
  """

  use CaravelaDemoWeb, :live_view

  alias CaravelaDemo.Library

  @impl true
  def mount(%{"id" => id}, _session, socket) do
    context = build_context(socket)

    case Library.get_publisher(id, context) do
      nil ->
        {:ok,
         socket
         |> assign(:context, context)
         |> assign(:publisher, nil)
         |> assign(:flash_message, "Not found")}

      entity ->
        {:ok,
         socket
         |> assign(:context, context)
         |> assign(:publisher, entity)
         |> assign(:flash_message, nil)}
    end
  end

  @impl true
  def handle_event("edit", %{"id" => id}, socket) do
    {:noreply, push_navigate(socket, to: "/library/publishers/" <> id <> "/edit")}
  end

  def handle_event("back", _params, socket) do
    {:noreply, push_navigate(socket, to: "/library/publishers")}
  end

  @impl true
  def render(assigns) do
    ~H"""
    <LiveSvelte.svelte
      name="library/PublisherShow"
      props={%{
        publisher: @publisher,
        flash_message: @flash_message
      }}
      socket={@socket}
    />
    """
  end

  # --- Internal --------------------------------------------------------

  defp build_context(socket) do
    %{
      current_user: socket.assigns[:current_user]
    }
  end

  # --- CUSTOM ---
  # Custom code below this line is preserved on regeneration.
end
