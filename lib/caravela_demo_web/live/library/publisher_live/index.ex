defmodule CaravelaDemoWeb.Library.PublisherLive.Index do
  @moduledoc """
  Generated by Caravela from CaravelaDemo.Domains.Library.

  LiveView that mounts the `library/PublisherIndex` Svelte component via
  LiveSvelte. List page for `publishers`: renders the collection,
  handles delete, and navigates to the edit route on the `edit` event.

  Regenerate with `mix caravela.gen.live CaravelaDemo.Domains.Library`.
  Custom code placed below the `# --- CUSTOM ---` marker is preserved.
  """

  use CaravelaDemoWeb, :live_view

  alias CaravelaDemo.Library

  @impl true
  def mount(_params, _session, socket) do
    context = build_context(socket)

    socket =
      socket
      |> assign(:context, context)
      |> assign(:loading, false)
      |> assign(:flash_message, nil)
      |> assign(:publishers, Library.list_publishers(context))

    {:ok, socket}
  end

  @impl true
  def handle_event("delete", %{"id" => id}, socket) do
    context = socket.assigns.context

    case Library.get_publisher(id, context) do
      nil ->
        {:noreply, assign(socket, :flash_message, "Not found")}

      entity ->
        case Library.delete_publisher(entity, context) do
          {:ok, _} ->
            {:noreply,
             socket
             |> assign(:flash_message, "Deleted")
             |> assign(:publishers, Library.list_publishers(context))}

          {:error, :unauthorized} ->
            {:noreply, assign(socket, :flash_message, "Not authorized")}

          {:error, reason} ->
            {:noreply, assign(socket, :flash_message, "Error: " <> inspect(reason))}
        end
    end
  end

  def handle_event("edit", %{"id" => id}, socket) do
    {:noreply, push_navigate(socket, to: "/library/publishers/" <> id <> "/edit")}
  end

  def handle_event("new", _params, socket) do
    {:noreply, push_navigate(socket, to: "/library/publishers/new")}
  end

  @impl true
  def render(assigns) do
    ~H"""
    <LiveSvelte.render
      name="library/PublisherIndex"
      props={%{
        publishers: @publishers,
        loading: @loading,
        flash_message: @flash_message
      }}
    />
    """
  end

  # --- Internal --------------------------------------------------------

  defp build_context(socket) do
    %{
      current_user: socket.assigns[:current_user]
    }
  end

  # --- CUSTOM ---
  # Custom code below this line is preserved on regeneration.
end
