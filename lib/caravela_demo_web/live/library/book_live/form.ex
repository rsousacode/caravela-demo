defmodule CaravelaDemoWeb.Library.BookLive.Form do
  @moduledoc """
  Generated by Caravela from CaravelaDemo.Domains.Library (`--with-domain`).

  Template-backed form LiveView. State and transitions live in
  `CaravelaDemoWeb.Library.BookLive.FormDomain`; this module wires I/O (Repo calls,
  navigation) and renders the Svelte component.

  Regenerate with `mix caravela.gen.live --with-domain CaravelaDemo.Domains.Library`.
  Custom code placed below the `# --- CUSTOM ---` marker is preserved.
  """

  use CaravelaDemoWeb, :live_view
  use Caravela.Live.Template, domain: CaravelaDemoWeb.Library.BookLive.FormDomain

  alias CaravelaDemo.Library
  alias CaravelaDemo.Library.Book

  @impl true
  def mount(params, _session, socket) do
    defaults = CaravelaDemoWeb.Library.BookLive.FormDomain.__caravela_live_state__()
    socket = Caravela.Live.Template.__assign_defaults__(socket, defaults)

    context = build_context(socket)
    {entity, attrs} = load_initial(params, context)
    changeset = Library.change_book(entity, attrs)
    errors = changeset_errors(changeset)

    {:ok,
     socket
     |> assign(:context, context)
     |> apply_updater(:load, entity: entity, attrs: attrs, errors: errors)}
  end

  @impl true
  def handle_event("validate", %{"field" => field, "value" => value}, socket) do
    key = String.to_existing_atom(field)
    socket = apply_updater(socket, :put_attr, field: key, value: value)

    changeset =
      socket.assigns.book
      |> Library.change_book(socket.assigns.attrs)

    {:noreply, assign(socket, :errors, changeset_errors(changeset))}
  end

  def handle_event("save", _params, %{assigns: %{book: entity}} = socket) do
    context = socket.assigns.context
    attrs = socket.assigns.attrs
    socket = apply_updater(socket, :mark_saving)

    result =
      if new?(entity) do
        Library.create_book(attrs, context)
      else
        Library.update_book(entity, attrs, context)
      end

    case result do
      {:ok, _saved} ->
        {:noreply, push_navigate(apply_updater(socket, :mark_saved), to: "/library/books")}

      {:error, :unauthorized} ->
        {:noreply, apply_updater(socket, :set_flash, "Not authorized")}

      {:error, %Ecto.Changeset{} = cs} ->
        {:noreply, apply_updater(socket, :set_errors, changeset_errors(cs))}

      {:error, reason} ->
        {:noreply, apply_updater(socket, :set_flash, "Error: " <> inspect(reason))}
    end
  end

  def handle_event("cancel", _params, socket) do
    {:noreply, push_navigate(socket, to: "/library/books")}
  end

  @impl true
  def render(assigns) do
    ~H"""
    <LiveSvelte.svelte
      name="library/BookForm"
      props={%{
        book: @attrs,
        errors: @errors,
        saving: @saving,
        flash_message: @flash_message
      }}
      socket={@socket}
    />
    """
  end

  # --- Internal --------------------------------------------------------

  defp load_initial(%{"id" => id}, context) do
    case Library.get_book(id, context) do
      nil -> {%Book{}, %{}}
      entity -> {entity, entity_attrs(entity)}
    end
  end

  defp load_initial(_params, _context), do: {%Book{}, %{}}

  # Narrow the attrs map to the declared entity fields and normalise
  # struct values to wire-friendly primitives so the Svelte form inputs
  # don't receive `%Decimal{}` / `%DateTime{}` dumps.
  defp entity_attrs(entity) do
    [:title, :isbn, :published, :price]
    |> Enum.reduce(%{}, fn field, acc ->
      Map.put(acc, field, normalise_attr(Map.get(entity, field)))
    end)
  end

  defp normalise_attr(%Decimal{} = d), do: Decimal.to_string(d, :normal)
  defp normalise_attr(other), do: other

  defp new?(%Book{id: nil}), do: true
  defp new?(%Book{}), do: false

  defp changeset_errors(%Ecto.Changeset{} = cs) do
    Ecto.Changeset.traverse_errors(cs, fn {msg, opts} ->
      [
        Enum.reduce(opts, msg, fn {k, v}, acc ->
          String.replace(acc, "%{" <> Atom.to_string(k) <> "}", to_string(v))
        end)
      ]
    end)
  end

  defp build_context(socket) do
    %{
      current_user: socket.assigns[:current_user]
    }
  end

  # --- CUSTOM ---
  # Custom code below this line is preserved on regeneration.
end
